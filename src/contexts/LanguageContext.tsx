interface LanguageContextType {
  currentLanguage: Language;
  language: Language; // Alias for compatibility
  setLanguage: (language: Language) => void;
  t: (key: string, params?: Record<string, any>) => string;
  formatCurrency: (amount: number, currency?: string) => string; // Added for compatibility
  availableLanguages: { code: Language; name: string; flag: string }[];
}

const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

export const LanguageProvider = ({ children }: { children: ReactNode }) => {
  const [currentLanguage, setCurrentLanguage] = useState<Language>('fr');

  const t = (key: string, params?: Record<string, any>): string => {
    const translation = translations[currentLanguage]?.[key] || translations.fr[key];
    
    // Syst√®me anti-r√©gression : D√©tecter les cl√©s manquantes
    if (!translation) {
      if (process.env.NODE_ENV === 'development') {
        console.warn(`üö® MISSING TRANSLATION: "${key}" in language "${currentLanguage}"`);
        return `[MISSING: ${key}]`; // Affichage visible pour les d√©veloppeurs
      }
      return key; // Fallback en production
    }
    
    if (params) {
      return translation.replace(/\{(\d+)\}/g, (match, index) => {
        return params[index] || match;
      });
    }
    
    return translation;
  };

  const setLanguage = (language: Language) => {
    setCurrentLanguage(language);
    localStorage.setItem('kwenda-language', language);
  };

  const formatCurrency = (amount: number, currency = 'CDF'): string => {
    return `${amount.toLocaleString()} ${currency}`;
  };

  const availableLanguages = [
    { code: 'fr' as Language, name: 'Fran√ßais', flag: 'üá´üá∑' },
    { code: 'en' as Language, name: 'English', flag: 'üá∫üá∏' },
    { code: 'kg' as Language, name: 'Kikongo', flag: 'üá®üá©' },
    { code: 'lua' as Language, name: 'Tshiluba', flag: 'üá®üá©' },
    { code: 'sw' as Language, name: 'Kiswahili', flag: 'üáπüáø' }
  ];

  React.useEffect(() => {
    const savedLanguage = localStorage.getItem('kwenda-language') as Language;
    if (savedLanguage && ['fr', 'en', 'kg', 'lua', 'sw'].includes(savedLanguage)) {
      setCurrentLanguage(savedLanguage);
    }
    
    // Validation automatique des traductions en mode d√©veloppement
    logMissingTranslations();
  }, []);

  return (
    <LanguageContext.Provider value={{
      currentLanguage,
      language: currentLanguage, // Alias for compatibility
      setLanguage,
      t,
      formatCurrency,
      availableLanguages
    }}>
      {children}
    </LanguageContext.Provider>
  );
};

export const useLanguage = () => {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
};